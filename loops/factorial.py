# factorial with while loop
n = 4
fact = 1
i = 1
while i <= n:
    fact *= i
    i += 1
print("factorial : ", fact)

# factorial with for loop
n =5
fact  = 1
for i in range(1, n+1):
    fact *= i
print("Factorial: ", fact)